const handleServerResponse = (res: Response): Promise<any> => {
  return res.ok ? res.json() : Promise.reject(`Error: ${res.status}`);
};

export function request(
  url: RequestInfo | URL,
  options?: RequestInit
): Promise<any> {
  return fetch(url, options).then(handleServerResponse);
}

